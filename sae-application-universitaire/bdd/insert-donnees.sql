-- 7) Notes : une ligne par (étudiant, contrôle) via insertion ensembliste
--    Objectif : remplir Note sans écrire 2000 INSERT manuels, tout en gardant des notes stables/reproductibles.
INSERT INTO Note(idEtu, idControle, note)
SELECT
  e.idEtu,
  c.idControle,
  ROUND(
    LEAST(c.noteMaximale,
      GREATEST(0,
        12
        + ((e.idEtu % 10) - 4) * 0.55
        + ((c.idControle % 7) - 3) * 0.45
        + CASE
            WHEN m.nomMatiere LIKE 'MMI - Anglais%' THEN 0.8
            WHEN m.nomMatiere LIKE 'MMI - Développement web%' THEN 0.4
            WHEN m.nomMatiere LIKE 'MMI - Droit%' THEN -0.2
            ELSE 0
          END
      )
    ), 2
  ) AS note
FROM Etudiant e
JOIN Controle c ON 1=1
JOIN Matiere m ON m.idMatiere = c.idMatiere
WHERE e.idFormation = @idFormationMMI
  AND e.numSemestre = 1
  AND m.nomMatiere LIKE 'MMI - %';

